# Leia dois números e mostre:
# A soma
# A subtração
# A multiplicação
# A divisão
numero01 = int(input("digite o primeiro número: "))
numero02 = int(input("digite o segundo número: "))
soma = numero01 + numero02
subtracao = numero01 - numero02
multiplicacao = numero01 * numero02
divisao = numero01 / numero02
print(f'sua soma é: {soma} sua subtracao é: {subtracao} sua multiplicacao é: {multiplicacao} sua divisao é: {divisao}')

